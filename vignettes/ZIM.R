library(ZIM)

## ------------------------------------------------------------------------
data(syph)
count <- syph$a33
ar1 <- bshift(count > 0)
trend <- 1:length(count) / 1000
zim(count ~ ar1 + trend | trend)

## ------------------------------------------------------------------------
zim(count ~ ar1 + trend | trend, dist = "zinb")

